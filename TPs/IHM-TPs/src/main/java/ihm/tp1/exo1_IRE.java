    private int calculIRE(int temperature, int vitesseVent){
        double result = 13.12 + 0.62215 * temperature + 
                (0.3965 * temperature - 11.37) * Math.pow(vitesseVent, 0.16);
        return (int) result;
    }
    
    
    private void jButton_calculerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calculerActionPerformed
        // On récupère les valeurs dans jTextField_temperature et jTextField_vitesse
        String temp = jTextField_temperature.getText();
        String vitesse = jTextField_vitesse.getText();
        int temp_verif = verificationValeur(0, temp);
        int vitesse_verif = verificationValeur(1, vitesse);
        
        int res = calculIRE(temp_verif, vitesse_verif);
        
        jTextField_resultat.setText(String.valueOf(res));
        
    }//GEN-LAST:event_jButton_calculerActionPerformed

