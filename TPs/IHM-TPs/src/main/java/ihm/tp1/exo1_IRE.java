    private boolean verificationType(String str){
        try {
            int valeur = Integer.parseInt(str.trim()); // trim pour éviter les espaces accidentels
            return true;
        }
        catch(NumberFormatException e){
            return false;
        }
        
    }
    
    /* On vérifie si la valeur mise en entrée est valide ou non
     @param
        int type -> 0 pour température et 1 pour vitesse
        int valeur -> valeur à vérifier
     @return
        0 -> valeur n'est pas un chiffre
        -50 -> valeur inférieur à -50 pour la température
        5 -> valeur inférieur à 5 pour la vitesse
        10 -> valeur suppérieur à 10 pour la température
        175 -> valeur suppérieur à 175 pour la vitesse
    */
    private int verificationValeur(int type, String valeur){        
        // Valeur n'est pas un chiffre -> TODO
        if(!verificationType(valeur)){
            System.out.println("[WARNING] verificationType: " + valeur + "n'est pas une valeur");
            return 0;
        }
        
        int valeur_int = Integer.parseInt(valeur);
        
        int res = 0;
        switch (type){
            case 0: // température
                // valeur inférieur à -50
                if (valeur_int < -50) res = -50;
                // valeur suppérieur à 10
                if (valeur_int > 10) res = 10;
                break;
            
            case 1: // vitesse
                // valeur inférieur à 5
                if (valeur_int < 5) res = 5;
                // valeur suppérieur à 175
                if (valeur_int > 175) res = 175;
                break;
        }
        
        return res;
 
    }
    
