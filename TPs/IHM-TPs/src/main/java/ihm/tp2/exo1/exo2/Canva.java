    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        // Quand on fait un clic gauche, on réccupère les coordonnée de la souris
        if(SwingUtilities.isLeftMouseButton(evt)){
            // On regarde quel bouton est sélectionnée pour les couleurs et l'épaisseur
            Color color = currentColor;
            int thickness = currentThickness;
            
            // On initialise les coordonnées de la souris et colorPoint
            Point point = evt.getPoint();
            ColorPoint colorPoint = new ColorPoint(color,point,thickness);
            
            // On les stock ensuite dans la classe ColorPoint
            listColorPoints.add(colorPoint);
            
            // On oublie pas de repaint
            this.repaint();
        }
    }//GEN-LAST:event_formMousePressed

