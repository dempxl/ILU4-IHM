    @Override
    public void paintComponent(Graphics g){
        super.paintComponent(g);
       /**
        * On parcours le liste de ColorPoint et pour chaque point:
        *   - Définit la couleur sur le Graphics
        *   - Définit l'épaisseur (on oublie pas de caster g en Graphics2D)
        *   - On dessine un ovale centré sur point.x et point.y avec la taille et l'épaisseur
        */
       Graphics2D g2d = (Graphics2D) g;
       for (ColorPoint point : listColorPoints){
           int x = (int) point.getPoint().getX();
           int y = (int) point.getPoint().getY();
           Color color = point.getColor();
           int thickness = point.getThickness();
           
           g2d.setColor(color);
           // Contrairement à drawOval, là on dessine un oval rempli
           g2d.fillOval(x, y, thickness, thickness);
       }
       
       
       
    }

