    @Override
    public void paintComponent(Graphics g){
        super.paintComponent(g);
 
        // 1. Dessiner toutes les lignes définitives
        for (ColorLine line : lines) {
            g.setColor(line.color);
            g.drawLine(line.start.x, line.start.y, line.end.x, line.end.y);
        }

        // 2. Ligne temporaire gauche (noir pendant le drag)
        if (startPoint_left != null && endPoint_left != null) {
            g.setColor(Color.BLACK);
            g.drawLine(startPoint_left.x, startPoint_left.y,
                       endPoint_left.x,   endPoint_left.y);
        }

        // 3. Ligne temporaire droite (noir pendant le drag)
        if (startPoint_right != null && endPoint_right != null) {
            g.setColor(Color.BLACK);
            g.drawLine(startPoint_right.x, startPoint_right.y,
                       endPoint_right.x,   endPoint_right.y);
        }
        
    }

