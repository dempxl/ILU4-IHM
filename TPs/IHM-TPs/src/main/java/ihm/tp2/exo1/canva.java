    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        // On fait un clic gauche
        if(SwingUtilities.isLeftMouseButton(evt)){
            startPoint_left = evt.getPoint();
        }
        // On fait un clic droite
        if(SwingUtilities.isRightMouseButton(evt)){
            startPoint_right = evt.getPoint();
        }
        this.repaint();
    }//GEN-LAST:event_formMousePressed

    private void formMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseReleased
        // On fait un clic gauche
        if(SwingUtilities.isLeftMouseButton(evt) && startPoint_left != null){
            // On rajoute à la liste des droites la nouvelle droite
            lines.add(new ColorLine(startPoint_left, evt.getPoint(), Color.RED));
            // On reset les coordonnées des points
            startPoint_left = null;
            endPoint_left = null;

        }
        // On fait un clic droite
        if(SwingUtilities.isRightMouseButton(evt) && startPoint_right != null){
            lines.add(new ColorLine(startPoint_right, evt.getPoint(), Color.BLUE));
            // On reset les coordonnées des points
            startPoint_right = null;
            endPoint_right = null;
        }
        // On oublie pas "d'update" la frame
        this.repaint();
    }//GEN-LAST:event_formMouseReleased

    private void formMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseDragged
        // On fait un clic gauche
        if(SwingUtilities.isLeftMouseButton(evt)){
            endPoint_left = evt.getPoint();
        }
        // On fait un clic droite
        if(SwingUtilities.isRightMouseButton(evt)){
            endPoint_right = evt.getPoint();
        }
        this.repaint();
    }//GEN-LAST:event_formMouseDragged

